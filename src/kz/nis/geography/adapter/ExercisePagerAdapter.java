package kz.nis.geography.adapter;

import java.util.ArrayList;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;
import android.support.v4.app.ListFragment;

public class ExercisePagerAdapter extends FragmentPagerAdapter {

	private ArrayList<ListFragment> fragments;

	public ExercisePagerAdapter(FragmentManager fm, ArrayList<ListFragment> fragments) {
		super(fm);
		this.fragments = fragments;
	}

	@Override
	public Fragment getItem(int position) {
		return fragments.get(position);
	}

	@Override
	public int getCount() {
		return fragments.size();
	}

}
